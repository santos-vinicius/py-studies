primeiro_valor = int(input('Digite um valor: '))
segundo_valor = int(input('Digite outro valor: '))

# comparação de valores

if primeiro_valor >= segundo_valor:
  print(f'O primeiro valor: "{primeiro_valor}" é maior ou igual que o segundo valor: "{segundo_valor}".')
else:
  print(f'O segundo valor: "{segundo_valor}" é maior ou igual que o primeiro valor: "{primeiro_valor}".')
